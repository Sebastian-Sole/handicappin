/**
 * emit-sql(): resolved `Course` -> a PL/pgSQL DO-block, byte-compatible with the
 * format produced by scripts/parse_scorecard.py so seed files stay uniform and
 * flow through scripts/build-seed.sh unchanged.
 */
import type { Course } from "../../types/scorecard-input";

function esc(s: string): string {
  return s.replace(/'/g, "''");
}

function fmtRating(n: number): string {
  // Ratings are numeric with one decimal in the seed (e.g. 72.4, 13.2).
  return n.toFixed(1);
}

function websiteLiteral(website: string | undefined): string {
  if (!website || website.trim() === "") return "null";
  const url = /^https?:\/\//i.test(website) ? website : `https://${website}`;
  return `'${esc(url)}'`;
}

/** Mirrors parse_scorecard.py: name.lower().replace(" ", "_"). */
export function slugify(name: string): string {
  return name.toLowerCase().replace(/ /g, "_");
}

export function emitSql(c: Course, opts: { nineHole: boolean }): string {
  const tees = c.tees ?? [];
  const L: string[] = [];

  L.push(`-- Course: ${c.name}`);
  L.push(`-- Location: ${c.city}, ${c.country}`);
  L.push(`-- Type: ${opts.nineHole ? "9-hole" : "18-hole"} course`);
  L.push(`-- Generated by course-ingest`);
  L.push("");

  L.push("do $$");
  L.push("declare");
  L.push("    v_course_id integer;");
  tees.forEach((_, i) => L.push(`    v_tee_id_${i + 1} integer;`));
  L.push("begin");
  L.push("");

  L.push("    -- Insert course");
  L.push(
    `    insert into public.course (name, city, country, website, "approvalStatus", "submittedBy")`,
  );
  L.push(
    `    values ('${esc(c.name)}', '${esc(c.city)}', '${esc(c.country)}', ${websiteLiteral(
      c.website,
    )}, '${c.approvalStatus}', null)`,
  );
  L.push("    returning id into v_course_id;");
  L.push("");

  tees.forEach((tee, i) => {
    const n = i + 1;
    L.push(`    -- Insert ${tee.name} tee (${tee.gender})`);
    L.push(`    insert into public."teeInfo" (`);
    L.push(`        "courseId", name, gender,`);
    L.push(`        "courseRating18", "slopeRating18",`);
    L.push(`        "courseRatingFront9", "slopeRatingFront9",`);
    L.push(`        "courseRatingBack9", "slopeRatingBack9",`);
    L.push(`        "outPar", "inPar", "totalPar",`);
    L.push(`        "outDistance", "inDistance", "totalDistance",`);
    L.push(`        "distanceMeasurement", "approvalStatus", "submittedBy"`);
    L.push(`    )`);
    L.push(`    values (`);
    L.push(`        v_course_id, '${esc(tee.name)}', '${tee.gender}',`);
    L.push(`        ${fmtRating(tee.courseRating18)}, ${tee.slopeRating18},`);
    L.push(`        ${fmtRating(tee.courseRatingFront9)}, ${tee.slopeRatingFront9},`);
    L.push(`        ${fmtRating(tee.courseRatingBack9)}, ${tee.slopeRatingBack9},`);
    L.push(`        ${tee.outPar}, ${tee.inPar}, ${tee.totalPar},`);
    L.push(`        ${tee.outDistance}, ${tee.inDistance}, ${tee.totalDistance},`);
    L.push(`        '${tee.distanceMeasurement}', '${tee.approvalStatus}', null`);
    L.push(`    )`);
    L.push(`    returning id into v_tee_id_${n};`);
    L.push("");

    L.push(`    -- Holes for ${tee.name} (${tee.gender})`);
    for (const h of tee.holes ?? []) {
      L.push(`    insert into public.hole ("teeId", "holeNumber", par, distance, hcp)`);
      L.push(`    values (v_tee_id_${n}, ${h.holeNumber}, ${h.par}, ${h.distance}, ${h.hcp});`);
    }
    L.push("");
  });

  L.push("    raise notice 'Successfully inserted course: %', v_course_id;");
  L.push("end $$;");

  return L.join("\n") + "\n";
}
