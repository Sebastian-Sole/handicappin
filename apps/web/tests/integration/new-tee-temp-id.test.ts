/**
 * Regression: submitting a scorecard whose tee carries a CLIENT-SIDE TEMP ID
 * must create the tee, not throw.
 *
 * The web/native scorecard forms assign new tees a temporary NEGATIVE id
 * (`useTeeManagement.generateTempId` → -1, -2, -3…) because the client can't
 * know the real auto-incremented id yet. When such a tee is submitted with no
 * matching approved tee in the DB, `round.submitScorecard` used to fall into
 * its "verify referenced tee by id" branch and throw
 *   "Approved, non-archived tee with ID -3 not found in database"
 * instead of inserting the brand-new pending tee. Block 3b now gates that
 * branch on `id > 0` (mirroring block 3a), so temp ids fall through to insert.
 *
 * Runs against the REAL local Supabase stack (same pattern as
 * shot-detail-persistence.test.ts); skips when no local stack is available.
 */
import { describe, test, expect, beforeAll, afterAll } from "vitest";
import { randomUUID } from "crypto";
import { createClient } from "@supabase/supabase-js";
import { and, eq, inArray } from "drizzle-orm";

import { createCallerFactory } from "@/server/api/trpc";
import { roundRouter } from "@/server/api/routers/round";
import type { Scorecard } from "@/types/scorecard-input";

const { db } = await import("@/db");
const { profile, course, teeInfo, hole, round } = await import("@/db/schema");

const supabaseUrl = process.env.NEXT_PUBLIC_SUPABASE_URL;
const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
const databaseUrl = process.env.DATABASE_URL;
const isLocalStack =
  !!databaseUrl?.includes("127.0.0.1") || !!databaseUrl?.includes("localhost");
const hasRealSupabase =
  !!supabaseUrl &&
  !supabaseUrl.includes("dummy") &&
  !!serviceRoleKey &&
  !serviceRoleKey.includes("dummy");
const describeIfLocal =
  hasRealSupabase && isLocalStack ? describe : describe.skip;

const TEST_EMAIL = "new-tee-temp-id-test@handicappin.local";
// A client temp id: negative, as generated by useTeeManagement.
const TEMP_TEE_ID = -3;

let userId: string;
let courseId: number;
const createdRoundIds: number[] = [];
const createdTeeIds: number[] = [];

const createCaller = createCallerFactory(roundRouter);

function buildScorecard(): Scorecard {
  const holes = Array.from({ length: 18 }, (_, i) => ({
    id: undefined,
    teeId: TEMP_TEE_ID,
    holeNumber: i + 1,
    par: 4,
    hcp: i + 1,
    distance: 350,
  }));
  return {
    userId,
    course: {
      id: courseId,
      name: "New Tee Temp Id Test Course",
      approvalStatus: "approved",
      country: "Norway",
      city: "Oslo",
      tees: undefined,
    },
    teePlayed: {
      // The crux: a brand-new tee the user just built in the form, still
      // carrying its negative placeholder id and pending status.
      id: TEMP_TEE_ID,
      courseId,
      name: "Freshly Added Tee",
      gender: "mens",
      courseRating18: 72.0,
      slopeRating18: 130,
      courseRatingFront9: 36.0,
      slopeRatingFront9: 130,
      courseRatingBack9: 36.0,
      slopeRatingBack9: 130,
      outPar: 36,
      inPar: 36,
      totalPar: 72,
      outDistance: 3150,
      inDistance: 3150,
      totalDistance: 6300,
      distanceMeasurement: "yards",
      approvalStatus: "pending",
      holes,
    },
    scores: Array.from({ length: 18 }, () => ({ strokes: 5, hcpStrokes: 0 })),
    teeTime: "2026-07-01T10:00:00.000Z",
    approvalStatus: "pending",
    notes: undefined,
    nineHoleSection: undefined,
  };
}

describeIfLocal("submitScorecard new tee with temp id (real local Supabase)", () => {
  beforeAll(async () => {
    const admin = createClient(supabaseUrl!, serviceRoleKey!, {
      auth: { autoRefreshToken: false, persistSession: false },
    });
    const { data: usersPage } = await admin.auth.admin.listUsers();
    const existing = usersPage?.users.find((u) => u.email === TEST_EMAIL);
    if (existing) {
      await db.delete(profile).where(eq(profile.id, existing.id));
      await admin.auth.admin.deleteUser(existing.id);
    }
    const { data: created, error } = await admin.auth.admin.createUser({
      email: TEST_EMAIL,
      email_confirm: true,
      password: randomUUID(),
    });
    if (error || !created.user) {
      throw new Error(`Failed to create test user: ${error?.message}`);
    }
    userId = created.user.id;
    await db.insert(profile).values({
      id: userId,
      email: TEST_EMAIL,
      name: "New Tee Temp Id Test",
      verified: true,
      planSelected: "unlimited",
      subscriptionStatus: "active",
    });

    // An approved course with NO matching approved tee — so resolution can't
    // find an existing tee and must insert the submitted one.
    const [createdCourse] = await db
      .insert(course)
      .values({
        name: "New Tee Temp Id Test Course",
        country: "Norway",
        city: "Oslo",
        approvalStatus: "approved",
      })
      .returning();
    courseId = createdCourse!.id;
  }, 30_000);

  afterAll(async () => {
    if (!userId) return;
    const admin = createClient(supabaseUrl!, serviceRoleKey!, {
      auth: { autoRefreshToken: false, persistSession: false },
    });
    if (createdRoundIds.length > 0) {
      await db.delete(round).where(inArray(round.id, createdRoundIds));
    }
    for (const id of createdTeeIds) {
      await db.delete(hole).where(eq(hole.teeId, id));
      await db.delete(teeInfo).where(eq(teeInfo.id, id));
    }
    if (courseId) {
      await db.delete(course).where(eq(course.id, courseId));
    }
    await db.delete(profile).where(eq(profile.id, userId));
    await admin.auth.admin.deleteUser(userId);
  }, 30_000);

  function buildCaller() {
    const supabase = createClient(supabaseUrl!, serviceRoleKey!, {
      auth: { autoRefreshToken: false, persistSession: false },
    });
    return createCaller({
      user: { id: userId },
      supabase,
    } as unknown as Parameters<typeof createCaller>[0]);
  }

  test("temp-negative-id tee is inserted (not rejected); round + tee persist with a real id", async () => {
    const caller = buildCaller();

    // Before the fix this threw "…tee with ID -3 not found in database".
    const created = await caller.submitScorecard(buildScorecard());
    createdRoundIds.push(created.id);

    // A real, positive-id pending tee was created for this course.
    const tees = await db
      .select()
      .from(teeInfo)
      .where(
        and(
          eq(teeInfo.courseId, courseId),
          eq(teeInfo.name, "Freshly Added Tee"),
        ),
      );
    expect(tees).toHaveLength(1);
    const inserted = tees[0]!;
    createdTeeIds.push(inserted.id);
    expect(inserted.id).toBeGreaterThan(0); // never the -3 temp id
    expect(inserted.approvalStatus).toBe("pending");

    // The round references that real tee id — no negative id leaked through.
    const [savedRound] = await db
      .select()
      .from(round)
      .where(eq(round.id, created.id));
    expect(savedRound!.teeId).toBe(inserted.id);

    // Its 18 holes were persisted under the new tee.
    const insertedHoles = await db
      .select()
      .from(hole)
      .where(eq(hole.teeId, inserted.id));
    expect(insertedHoles).toHaveLength(18);
  });
});
